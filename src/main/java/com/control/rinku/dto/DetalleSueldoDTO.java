package com.control.rinku.dto;

public class DetalleSueldoDTO {
	private String horasTrabajadas;
	private String totalPorEntregas;
	private String totalPorBonos;
	private String retencion;
	private String vales;
	private String sueldoTotal;
	
	public String getHorasTrabajadas() {
		return horasTrabajadas;
	}
	public void setHorasTrabajadas(String horasTrabajadas) {
		this.horasTrabajadas = horasTrabajadas;
	}
	public String getTotalPorEntregas() {
		return totalPorEntregas;
	}
	public void setTotalPorEntregas(String totalPorEntregas) {
		this.totalPorEntregas = totalPorEntregas;
	}
	public String getTotalPorBonos() {
		return totalPorBonos;
	}
	public void setTotalPorBonos(String totalPorBonos) {
		this.totalPorBonos = totalPorBonos;
	}
	public String getRetencion() {
		return retencion;
	}
	public void setRetencion(String retencion) {
		this.retencion = retencion;
	}
	public String getVales() {
		return vales;
	}
	public void setVales(String vales) {
		this.vales = vales;
	}
	public String getSueldoTotal() {
		return sueldoTotal;
	}
	public void setSueldoTotal(String sueldoTotal) {
		this.sueldoTotal = sueldoTotal;
	}
	
	
}
